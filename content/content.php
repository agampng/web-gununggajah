<h1>Beranda</h1>
<p>Gununggajah merupakan desa kecil yang berada di timur kota Klaten,tepatnya berada di wilayah kecamatan Bayat Kabupaten Klaten, Desa Gununggajah Terletak pada 164 M diatas permukaan Laut, membuat desa ini memiliki curah hujan rata-rata tiap tahunnya pada kisaran 30,65 mm/th. Sedangkan suhu udara berada pada kisaran 25-32C.Latitude:-7,76697 Longitude:110,67124 S7’ 46’1,108” E 110’40’16,45212”</p>
<p>Merupakan desa kecil yang miskin dengan taraf hidup yang memprihatinkan,Gununggajah termasuk desa ZONA MERAH di wilayah kecamata Bayat dengan tingkat perekonomian dan Sumber Daya Manusia minim.</p>
<p>Sebelum desa Gununggajah beranjak dari ZONA MERAH, PAD Desa Gununggajah hanya 3 jt per tahun,Sehingga hanya berharap pada bantuan baik pemerintah Kabupaten,Provinsi maupun Pemerintah Pusat.</p>
