<h1>Tentang Kami</h1>
<p>Manajemen yang kita kembangkan dalam pengelolaan taman ini adalah <strong><u>Gununggajah</u></strong><strong><u> For All, and All For </u></strong><strong><u>Gununggajah</u></strong><strong><u>, atau istilah kita Manajemen Komunitas. </u></strong></p>
<p><strong><u>&nbsp;</u></strong></p>
<p>Kegiatan pengelolaan di awali dengan Kirab Budaya dan Launching Pengelolaan Taman Watuprau dan Bukit Cinta, yang terlaksana pada tanggal <strong>23 April 2017</strong>.</p>
<p>&nbsp;</p>
<p>Agar kegiatan pengelolaan Taman Watuprau dan Bukit Cinta ini menjadi lebih terprogram dan terencana, maka mulai medio Mei 2017, Pemerintah Desa Gununggajah melibatkan <strong>BUMDes GUMBREGAH </strong>Gununggajah untuk mendampingi Karang Taruna Argo Mudho Desa Gununggajah.</p>
<p>BUMDesa GUMBREGAH berdiri pada tanggal 7 April 2017, dan segera melakukan pengelolaan Bukit Cinta Watu Prahu.</p>
<p>&nbsp;</p>
<p>Setelah di launching pada bulan April 2017,peran Masyarakat Gununggajah mulai terlihat aktif,dengan adanya wisata mulai mengurangi angka pengangguran khususnya di desa Gununggajah serta meningkatkan pendapatan per kapita dan meningkatkan Pendapatan Asli Desa (PAD)</p>
<p>&nbsp;</p>
<p>Berawal dari Bukit Cinta Watu Prahu perlahan Desa Gununggajah mulai Keluar dari ZONA MERAH,dan mulai bangkit dari keterpurukan.</p>
<p>Terbukti Munculnya Bukit Cinta Watu Prahu membawa dampak, meningkatkan kepercayaan diri masyarakat Gununggajah untuk menggali setiap potensi yang ada.</p>
<p>Maka munculah Pesona Puncak Arjuna yang dikelola oleh Pokdarwis Madukara di RW 1, dan Wisata Kedung Wewe Indah yang dikelola oleh Pokdarwis di RW 2.</p>
